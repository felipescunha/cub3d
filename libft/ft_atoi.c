#include "libft.h"

int	ft_atoi(const char *str)
{
	size_t	i;
	size_t	number;
	size_t	signal;

	number = 0;
	i = 0;
	signal = 1;
	while ((str[i] == ' ') || (str[i] >= 9 && str[i] <= 13))
		i++;
	if ((str[i] == '-') || (str[i] == '+'))
	{
		if (str[i] == '-')
			signal *= -1;
		i++;
	}
	while (str[i] >= '0' && str[i] <= '9')
	{
		number = number * 10 + (str[i] - '0');
		i++;
	}
	return (number * signal);
}	
